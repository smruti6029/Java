//Write a program to showcase the usage of functions such as Math.abs(), Math.sqrt(), 
//Math.pow(), Math.max(), Math.min(), Math.random(), Math.floor(), Math.ceil(), and Math.round(). 
package MathPack;

public class MathMethod {

	public static void main(String[] args) {
//		int nummber=5;
		double number=6;
		System.out.println(Math.abs(number));
		System.out.println(Math.sqrt(number));
		System.out.println(Math.pow(number,2));
		System.out.println(Math.max(number,2));
		System.out.println(Math.min(20, 6));
		double s=Math.random();
		System.out.println(s);
		System.out.println(Math.floor(2.3));
		System.out.println(Math.ceil(2.3));
		System.out.println(Math.round(2.3));
		
		
		
		
	

	}

}
